function translateArabicToEnglish(arabicText) {
  // البحث عن المفتاح الذي يحتوي على النص العربي المُدخل
  const entry = Object.entries(languages.arabic).find(
    ([key, value]) => value.trim() === arabicText.trim()
  );

  if (entry) {
    const key = entry[0];
    const englishValue = languages.english[key];
    return englishValue;
  } else {
    console.log(`لم يتم العثور على ترجمة لـ "${arabicText}"`);
  }
}
